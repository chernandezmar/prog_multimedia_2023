public class Prog_multimedia_2023 {

    public static void main(String[] args) {
        int var = 35;
        int resto = (var % 2);
        
        /* OPOERADORES ARITMETICOS
           + -> suma   
           - -> resta
           / -> division
           % -> mod o resto de la division
           * -> multiplicacion
        */
        if (resto == 0) {
            /*
            == -> exactamente igual
            >= -> mayor o igual
            >  -> mayor que
            <= -> menor o igual
            <  -> menor que
            */
            System.out.println("numero par");
        }
        else {
            System.out.println("numero impar");
        }
        
    }
}
